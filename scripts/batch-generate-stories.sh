# 生成故事函數
generate_story() {
    local num=$1
    local theme=${THEMES[$num]}
    local location=${LOCATIONS[$num]}
    local filename="photo${num}-story.md"
    local filepath="$STORIES_DIR/$filename"
    
    # 避免覆蓋已存在的文件
    if [ -f "$filepath" ]; then
        echo "⏭️  跳過 photo$num（已存在）"
        return
    fi
    
    cat > "$filepath" << EOF
# ${theme} 

**照片：** photo${num}.jpg  
**地點：** ${location}  
**時間：** 2026 年 3 月  
**主題：** ${theme}

---

## 故事

在${location}，家人們一起度過了${theme}的美好時光。笑容在臉上綻放，溫暖在心中流淌。

